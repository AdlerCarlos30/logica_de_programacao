var dia = "sexta";

switch (dia) {
    case "segunda":
    console.log("Dia de Começar a Semana");
    break;

    case "terça":
    console.log("Hora de Aprender Coisas Novas");
    break;

    case "quarta":
    console.log("Metade da semana, continue firme");
    break;

    case "quinta":
    console.log("Quase lá, mais um pouco de esorço");
    break;

    case "sexta":
    console.log("Dia de comemorar, o fim de semana chegou");
    break;

    default:
    console.log("Final de Semana Chegou")
}